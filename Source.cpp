#include <iostream>
using namespace std;
int main()
{
	int i, j, n, m, a[3][3];
	cin >> n;
	cin >> m;
	for (i = 0; i, n; i++) {
		for (j = 0; j < m; j++) {
			cin >> a[i][j];
		}

	}
	for (i = 0; i, n; i++) {
		for (j = 0; j < m; j++) {
			cout << a[i][j]<<"\t";

		}
		cout << endl;
	}
	


}

